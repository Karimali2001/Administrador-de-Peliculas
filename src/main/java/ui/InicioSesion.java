    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        //operaciones de archivo
        ArchivoSerializable aplicacionSerializable = new ArchivoSerializable();

        aplicacionSerializable.abrirArchivoLectura();
        UsuariosSer usuarios = aplicacionSerializable.ingresarRegistrosArreglo(); //ingreso los registros al arreglo

        aplicacionSerializable.cerrarArchivo();

        //guardo lo ingresado
        UsuarioSer usuarioVerificacion = new UsuarioSer(jTextField1.getText(), jPasswordField1.getText());
        boolean esUsuario = false; //bandera de si el usuario ingreso

        //verifico que el nombre de usuario 
        int indice = usuarios.consultarUsuario(usuarioVerificacion.getUsuario());
        if (indice == -1) {
            jLabel4.setText("Usuario no existente");
        } else {
            jLabel4.setText("");
            if (usuarios.consultarClave(indice, usuarioVerificacion.getClave())) {
                jLabel5.setText("");
                esUsuario = true;
            } else {
                jLabel5.setText("Clave ingresada incorrecta");
            }
        }

        //borro lo escrito por el usuario
        jTextField1.setText("");
        jPasswordField1.setText("");

        if (esUsuario) {
            this.setVisible(false);
            new Menu().setVisible(true);
        }

    }//GEN-LAST:event_jButton1MouseClicked

